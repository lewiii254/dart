// numbers
void main() {
  int num1 = 500;
  double num2 = 500.55;

  print(num2);
  print(num1);

  // perform aerithmetics
  double sum = num2 + num1;
  double difference = num2 - num1;
  double product = num2 * num1;
  double quotient = num2 / num1;
  double remainder = num2 % num1;
  print(sum);
  print(difference);
  print(remainder);
  print(product);
  print(quotient);
}
